// WHILE
console.log('WHILE');
let counter = 0;
while(counter< 3){
    counter++;
    console.log(counter)
}

// DO WHILE
console.log('DO WHILE');
let counter2 = 0;
do{
    console.log(counter2+1);
    counter2++;
}while(counter2<3);


// FOR
console.log('FOR');

for(let i = 0; i<5 ; i++){
    console.log(i+1);
}
console.log('End of FOR CYCLE');